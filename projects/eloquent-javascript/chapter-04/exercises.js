function reverseArray(arr) {
// need to create an array literal to contain elements 
var newArr = []
// will need to push all elements from given array backwards to new array
for(var i = arr.length-1; i >= 0; i--){
  newArr.push(arr[i]);
}
// return new array 
return newArr;
  
}

