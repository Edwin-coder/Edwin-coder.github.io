function flatten(array) {
  // will need a new array to contain all elements 
  var newArr = [];
  // will need two loops to work with nested arrays 
for(var i = 0; i < array.length;i++){
  for(var j = 0; j < array[i].length; j++){
    newArr.push(array[i][j])
  }
}
// return newArr
return newArr
}

